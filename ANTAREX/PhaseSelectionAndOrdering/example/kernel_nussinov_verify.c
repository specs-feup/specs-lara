static
void *
xmalloc (size_t num)
{
  void* cur = NULL;
  int ret = posix_memalign (&cur, 32, num);
  if (! cur || ret)
    {
      fprintf (stderr, "[PolyBench] posix_memalign: cannot allocate memory");
      exit (1);
    }
  return cur;
}


